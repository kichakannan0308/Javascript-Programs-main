// The trim() method removes whitespace from both sides of a string

//trim()
let x1="      Narmadha from pondicherry    ";
let ans1=x1.trim();
console.log(ans1);

//trimstart()
let x2="      Narmadha from pondicherry    ";
let ans2=x2.trimStart();
console.log(ans2);

//trimend()

let x3="      Narmadha from pondicherry    ";
let ans3=x3.trimEnd();
console.log(ans3);

